        public int[] anchoMapas = { 80, 100, 125 };
        public int[] altoMapas = { 40, 50, 60 };


        public int getTopTerreno(int indiceMapa, int indiceTerreno)
        {
            int fila =  indiceTerreno / (indiceMapa +3);
            int top=10 + 8*fila;
            return top;
        }

        public int getLeftTerreno(int indiceMapa, int indiceTerreno)
        {
            int columna = indiceTerreno % (indiceMapa + 3);
            int left = 3 + 24 * columna;
            return left;
        }

        public int getTopDatosTerreno(int indiceMapa, int indiceTerreno, int indiceDato)
        {
            int fila = indiceTerreno / (indiceMapa + 3);
            int top = 10 + 8 * fila + indiceDato;
            return top;
        }

        public int getLeftDatosTerreno(int indiceMapa, int indiceTerreno, int indiceDato)
        {
            int columna = indiceTerreno % (indiceMapa + 3);
            int left = 16 + 24 * columna;
            return left;
        }



